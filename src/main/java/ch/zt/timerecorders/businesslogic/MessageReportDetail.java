package ch.zt.timerecorders.businesslogic;

public class MessageReportDetail {
	
	public Long arbeitstagID;
	public int vmAnfang;
	public int nmAnfang;
	public int vmEnde;
	public int nmEnde;
	public int datevon;
	public int datebis;
	
	public Long getArbeitstagID() {
		return arbeitstagID;
	}
	public void setArbeitstagID(Long arbeitstagID) {
		this.arbeitstagID = arbeitstagID;
	}
	public int getVmAnfang() {
		return vmAnfang;
	}
	public void setVmAnfang(int vmAnfang) {
		this.vmAnfang = vmAnfang;
	}
	public int getNmAnfang() {
		return nmAnfang;
	}
	public void setNmAnfang(int nmAnfang) {
		this.nmAnfang = nmAnfang;
	}
	public int getVmEnde() {
		return vmEnde;
	}
	public void setVmEnde(int vmEnde) {
		this.vmEnde = vmEnde;
	}
	public int getNmEnde() {
		return nmEnde;
	}
	public void setNmEnde(int nmEnde) {
		this.nmEnde = nmEnde;
	}
	public int getDatevon() {
		return datevon;
	}
	public void setDatevon(int datevon) {
		this.datevon = datevon;
	}
	public int getDatebis() {
		return datebis;
	}
	public void setDatebis(int datebis) {
		this.datebis = datebis;
	}

}
